<div class="p-4 bg-blue-50 border-l-4 border-blue-500 text-blue-700">
    <h2 class="font-bold text-lg">Purpose of Product Deduction and Addition</h2>
    <p class="mt-2">
        Use the <strong>Addition</strong> type when increasing stock (e.g., receiving new inventory) and the
        <strong>Deduction</strong> type when reducing stock (e.g., due to sales, damage, or expired items).
    </p>
    <h3 class="font-bold mt-3">Examples:</h3>
    <ul class="list-disc pl-5 mt-2">
        <li>If you received 50 new items, select <strong>Addition</strong> and enter 50 as the quantity.</li>
        <li>If 10 items were sold or damaged, select <strong>Deduction</strong> and enter 10 as the quantity.</li>
        <li>  <strong>Note</strong> For customer order we are automatically deduct inventory  .</li>
    </ul>
</div>